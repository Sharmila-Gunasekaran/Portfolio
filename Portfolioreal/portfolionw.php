    function toggleDescription(shortdescription, fulldescription, showMoreButton) {
    var shortDescription = document.querySelector('.' + shortdescription);
    var fullDescription = document.querySelector('.'+fulldescription);
    var showMoreButton = document.getElementById(showMoreButton);

    if (fullDescription.style.display === 'none') {
        fullDescription.style.display = 'block';
        shortDescription.style.display = 'none';
        showMoreButton.textContent = 'Show Less';
    } else {
        fullDescription.style.display = 'none';
        shortDescription.style.display = 'block';
        showMoreButton.textContent = 'Show More';
    }
}
function mailcheck(emailid){
  let testmymail = /^([a-zA-Z0-9+-_%]+)@([a-zA-Z0-9.-]+).([a-zA-Z])(?:\.[a-zA-Z]{2,})?(?:\.[a-zA-Z]{2,})?$/;
  return testmymail.test(emailid);
}
